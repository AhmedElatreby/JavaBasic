package AccessModifiers.package1;


public class Access1 
{
    /*  there are four type of access modifier they are
        - Default
        - public
        - private
        - protected  
    */ 
    
    // this is a default access modifier as we did not put any of them
    int hours = 5;
    int minutes = 45;

    // this is a public access modifier 
    public int hoursToday = 5;
    public int minutesToday = 45;
    
    // this is a private access modifier 
    private int hoursYesterday = 5;
    private int minutesYesterday = 45;

    // this is a protected access modifier 
    protected int hoursTomorrow = 5;
    protected int minutesTomorrow = 45;
}
